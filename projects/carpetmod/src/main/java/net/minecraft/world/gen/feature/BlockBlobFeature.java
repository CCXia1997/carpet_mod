package net.minecraft.world.gen.feature;

import java.util.Random;
import net.minecraft.block.Block;
import net.minecraft.init.Blocks;
import net.minecraft.util.math.BlockPos;
import net.minecraft.world.IWorld;
import net.minecraft.world.gen.IChunkGenSettings;
import net.minecraft.world.gen.IChunkGenerator;

public class BlockBlobFeature extends Feature<BlockBlobConfig>
{
    public boolean place(IWorld worldIn, IChunkGenerator <? extends IChunkGenSettings > generator, Random rand, BlockPos pos, BlockBlobConfig config)
    {
        while (true)
        {
            label50:
            {
                if (pos.getY() > 3)
        {
            if (worldIn.isAirBlock(pos.down()))
                {
                    break label50;
                }

                Block block = worldIn.getBlockState(pos.down()).getBlock();

                if (block != Blocks.GRASS_BLOCK && !Block.isDirt(block) && !Block.isRock(block))
                {
                    break label50;
                }
            }

            if (pos.getY() <= 3)
        {
            return false;
        }

        int i1 = config.startRadius;

        for (int i = 0; i1 >= 0 && i < 3; ++i)
        {
            int j = i1 + rand.nextInt(2);
                int k = i1 + rand.nextInt(2);
                int l = i1 + rand.nextInt(2);
                float f = (float)(j + k + l) * 0.333F + 0.5F;

                for (BlockPos blockpos : BlockPos.getAllInBox(pos.add(-j, -k, -l), pos.add(j, k, l)))
                {
                    if (blockpos.distanceSq(pos) <= (double)(f * f))
                    {
                        worldIn.setBlockState(blockpos, config.block.getDefaultState(), 4);
                    }
                }

                pos = pos.add(-(i1 + 1) + rand.nextInt(2 + i1 * 2), 0 - rand.nextInt(2), -(i1 + 1) + rand.nextInt(2 + i1 * 2));
            }

            return true;
            }
            pos = pos.down();
        }
    }
}
